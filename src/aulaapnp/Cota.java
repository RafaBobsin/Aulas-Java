package aulaapnp;

public class Cota {
    
    double posX, posY, altura;
    
    void definePosX(double posX){
        this.posX = posX;
    }
    
    double getPosX(){
        return posX;
    }
    
    void definePosY(double posY){
        this.posY = posY;
    }
    
    double getPosY(){
        return posX;
    }
    
    void defineAltura(double altura){
        this.altura = altura;
    }
    
    double getAltura(){
        return altura;
    }
    
}
